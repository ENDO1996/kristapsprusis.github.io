function escapeHtml(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function renderInlineIllustration(passage, index) {
  const illustration = inlineIllustrations[`${passage.canto}:${index}`];
  if (!illustration) {
    return "";
  }

  return `
    <details class="inline-illustration">
      <summary>
        <span>Illustration</span>
        <strong>${escapeHtml(illustration.title)}</strong>
      </summary>
      <figure>
        <img src="${escapeHtml(illustration.image)}" alt="${escapeHtml(illustration.title)}" data-fullscreen-image="true" data-caption="${escapeHtml(`${illustration.title}. ${illustration.caption} ${illustration.source}.`)}">
        <figcaption>
          ${escapeHtml(illustration.caption)}
          <span>${escapeHtml(illustration.source)}</span>
        </figcaption>
      </figure>
    </details>
  `;
}

function render() {
  const query = searchInput.value.trim().toLowerCase();
  const selectedCanto = Number(cantoSelect.value || 1);
  const selectedPassages = allPassages.filter((passage) => passage.canto === selectedCanto);
  const heroSrc = cantoHeroImages[selectedCanto] || "assets/dark-wood.svg";
  heroArt.src = heroSrc;
  heroArt.alt = cantoTitles[selectedCanto] || "Dante Inferno canto scene";
  heroArt.dataset.caption = `${cantoTitles[selectedCanto] || `Canto ${selectedCanto}`} chapter image.`;
  heroArt.classList.remove("flaxman-hero");
  heroSource.textContent = window.cantoHeroImages?.[selectedCanto] ? "Oil Paint chapter image" : (cantoHeroLabels[selectedCanto] || "Complete Inferno");

  reader.innerHTML = selectedPassages.map((passage, index) => {
    const haystack = `${passage.lines} ${passage.classic} ${passage.modern} ${passage.notes}`.toLowerCase();
    const hidden = query && !haystack.includes(query) ? " hidden" : "";
    const illustration = renderInlineIllustration(passage, index);
    const unitLabel = `Unit ${index + 1}`;
    const rangeLabel = `Lines ${passage.lines}`;
    return `
      <article class="passage${hidden}" data-unit="${index + 1}">
        <section class="cell classic">
          <div class="cell-header"><span>Longfellow</span><span class="line-range">${unitLabel} · ${rangeLabel}</span></div>
          <div class="cell-body">${escapeHtml(passage.classic)}</div>
        </section>
        <section class="cell modern">
          <div class="cell-header"><span>Modern English</span><span class="line-range">${unitLabel} · ${rangeLabel}</span></div>
          <div class="cell-body">${escapeHtml(passage.modern)}</div>
        </section>
        <section class="cell notes">
          <div class="cell-header"><span>Notes</span><span class="line-range">${unitLabel} · ${rangeLabel}</span></div>
          <div class="cell-body">${passage.notes}${illustration}</div>
        </section>
      </article>
    `;
  }).join("");
}

