function setLightboxZoom(zoom) {
  lightboxZoom = Math.min(2.5, Math.max(1, Number(zoom.toFixed(2))));
  imageLightbox.style.setProperty("--lightbox-zoom", lightboxZoom);
  zoomLevel.textContent = `${Math.round(lightboxZoom * 100)}%`;
}

function openImageLightbox(image, zoom = 1.15) {
  setLightboxZoom(zoom);
  lightboxImage.src = image.src;
  lightboxImage.alt = image.alt;
  lightboxCaption.textContent = image.dataset.caption || image.alt;
  imageLightbox.classList.add("open");
  imageLightbox.setAttribute("aria-hidden", "false");
  closeLightbox.focus();
}

function closeImageLightbox() {
  imageLightbox.classList.remove("open");
  imageLightbox.setAttribute("aria-hidden", "true");
  lightboxImage.src = "";
  lightboxImage.alt = "";
  lightboxCaption.textContent = "";
  setLightboxZoom(1);
}

