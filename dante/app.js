function setMobileView(view) {
  const anchor = getMobileUnitAnchor();
  mobileView = view;
  document.body.classList.toggle("mobile-view-classic", mobileView === "classic");
  document.body.classList.toggle("mobile-view-modern", mobileView === "modern");
  document.body.classList.toggle("mobile-view-notes", mobileView === "notes");

  mobileViewButtons.forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.mobileView === mobileView));
  });

  if (anchor) {
    requestAnimationFrame(() => restoreMobileUnitAnchor(anchor));
  }
}

function isMobileReaderMode() {
  return window.matchMedia("(max-width: 760px)").matches;
}

function getMobileUnitAnchor() {
  if (!isMobileReaderMode()) {
    return null;
  }

  const visiblePassages = [...reader.querySelectorAll(".passage:not(.hidden)")];
  if (!visiblePassages.length) {
    return null;
  }

  const targetY = getMobileReadingTop();
  let best = visiblePassages[0];
  let bestDistance = Number.POSITIVE_INFINITY;

  visiblePassages.forEach((passage) => {
    const rect = passage.getBoundingClientRect();
    const distance = Math.abs(rect.top - targetY);
    if (rect.bottom >= targetY && distance < bestDistance) {
      best = passage;
      bestDistance = distance;
    }
  });

  return {
    unit: best.dataset.unit
  };
}

function restoreMobileUnitAnchor(anchor) {
  const passage = reader.querySelector(`.passage[data-unit="${anchor.unit}"]:not(.hidden)`);
  if (!passage) {
    return;
  }

  const top = passage.getBoundingClientRect().top + window.scrollY;
  window.scrollTo({
    top: Math.max(0, top - getMobileReadingTop()),
    behavior: "auto"
  });
}

function getMobileReadingTop() {
  const controlsBottom = controls?.getBoundingClientRect().bottom || 0;
  return Math.ceil(controlsBottom + 12);
}

